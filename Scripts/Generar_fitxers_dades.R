##-- Function ------------------------------------------------------------------
write_table_PE <- function(x,file_name){
  write.table(x         = x,
              file      = paste0("../Dades/",file_name,".csv"),
              sep       = ',',
              col.names = TRUE,
              row.names = FALSE,
              quote     = FALSE)
}
  
